package com.sucy.skill.api.enums;

/**
 * Direction used in the Particle class that are defined by what directions it uses for 2D shapes
 */
public enum Direction
{

    /**
     * X-axis and Y-axis
     */
    XY,

    /**
     * Y-axis and Z-axis
     */
    YZ,

    /**
     * X-axis and Z-axis
     */
    XZ
}
